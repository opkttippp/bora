            Лэптоп (англ. laptop — lap — колени сидящего человека, top — верх, «наколенник») — более широкий термин, он
            применяется как к ноутбукам, так и нетбукам, смартбукам.
            К ноутбукам обычно относят лэптопы (часто употребляется «лаптоп»), выполненные в раскладном форм-факторе.
            Ноутбук переносят в сложенном виде, это позволяет защитить экран, клавиатуру и тачпад при транспортировке.
            Также это связано с удобством транспортировки (чаще всего ноутбук транспортируется в портфеле, что позволяет
            не держать его в руках, а повесить на плечо).
